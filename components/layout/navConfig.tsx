// Devuelve el href activo: el más largo que sea prefijo de la ruta actual.
// Así /clientes/saldos le gana a /clientes, y /clientes/123 activa /clientes.
export const computeActiveHref = (pathname: string, items: NavItem[]): string | null =>
  items
    .map((i) => i.href)
    .filter((href) => pathname === href || pathname.startsWith(href + '/'))
    .sort((a, b) => b.length - a.length)[0] ?? null;

